var y = 2
if (x > y) {
  console.log("xはyより大きい");
}
/* Q2 xがyより小さい時にifの処理へ入るようにしてください。*/
var x = 1
var y = 3
if (x < y) {
  console.log("xはyより小さい");
}
/* Q3 xがy以上の時にifの処理へ入るようにしてください。*/
